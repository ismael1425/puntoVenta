    private void txtCodeProductKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodeProductKeyPressed
        if(evt.getKeyCode()== KeyEvent.VK_ENTER){
            String result="";
            String product = txtCodeProduct.getText();
            
            fProducts func = new fProducts();
            
            result = "1 " + func.findProduct(product)[1]+" "+func.findProduct(product)[2];
            
            listModel.addElement(result);
            txtCodeProduct.setText("");
        }
    }//GEN-LAST:event_txtCodeProductKeyPressed

