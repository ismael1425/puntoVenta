    private void btnRemoveProductMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRemoveProductMouseClicked
        if(listProducts.getSelectedIndex()==-1){
            JOptionPane.showMessageDialog(rootPane, "No ha selecionado un producto");
        }else{
            int res=JOptionPane.showConfirmDialog(null,"Â¿Desea borrar el producto de la lista?");
            if(res==0){
                int pos = listProducts.getSelectedIndex();
                listModel.remove(pos);

            }
        }
        
        
    }//GEN-LAST:event_btnRemoveProductMouseClicked

